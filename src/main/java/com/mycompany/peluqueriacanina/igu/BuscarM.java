    private void cargarTabla() {
        //definit modelo que tiene la tabla
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            
            
            @Override
            public boolean isCellEditable(int row, int colums){
               return false;
            }
            
        };
        //establecemos los nombres de las columas
        String titulos[] = {"Num", "Nombre", "Color", "Raza", "Alergico", "At. Esp.", "Due√±o", "Cel"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        //carga datos desde BD
        List <Mascota> listaMascotas = control.traerMascotas();
        //recorrer la lista y mostrar cada uno de los elementos en la tabla
        if(listaMascotas!=null){
            for(Mascota masco : listaMascotas){
               Object[] objeto ={masco.getNum_Cleinte(),masco.getNombre(),masco.getColor(),
                   masco.getRaza(),masco.getAlergico(),masco.getAlergico(),masco.getAtencion_especial(),
                   masco.getUnDuenio().getNombre(),masco.getUnDuenio().getCelDuenio()};
               
               modeloTabla.addRow(objeto);
               
            }
        }
        
        tablaMascotas.setModel(modeloTabla);
    }

